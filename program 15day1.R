# create three vectors of different data types
numeric_vec <- c(1, 2, 3, 4, 5)
character_vec <- c("apple", "banana", "cherry")
logical_vec <- c(TRUE, FALSE, TRUE, FALSE)

# display the content of the vectors
print(numeric_vec)
print(character_vec)
print(logical_vec)

# display the data type of the vectors
print(typeof(numeric_vec))
print(typeof(character_vec))
print(typeof(logical_vec))